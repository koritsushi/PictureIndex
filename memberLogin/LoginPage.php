    <script>
        $('#login').click(function(event) {
            event.preventDefault();
            var form = $('#mmLogin');
            var formData = new FormData(form[0]);


            $.ajax({
                type: "POST",
                url: "login.php",
                processData: false,
                contentType: false,
                data: formData,
                success: function(x) {
                    var delay = 500;
                    if (x == "Login successful!") {
                        Swal.fire(x, '', 'success').then((result => {
                            if (result.isConfirmed) {
                                window.location = "../user_management/shows/CurrentSeasonShowsPage.php";
                            } else {
                                setTimeout(function() {
                                    window.location = "../user_management/shows/CurrentSeasonShowsPage.php";
                                }, delay);
                            }
                        }))

                    } else if (x == "Account Deactived.") {
                        Swal.fire({
                            icon: 'error',
                            title: 'Your Account Has Been Deactived.',
                            html: '<pre>' + x + '</pre>',
                            customClass: {
                                popup: 'format-pre'
                            }
                        })
                    } else {
                        Swal.fire({
                            icon: 'error',
                            title: 'Somethings are wrong!',
                            html: '<pre>' + x + '</pre>',
                            customClass: {
                                popup: 'format-pre'
                            }
                        })
                    }
                }
            });

        });
    </script>



